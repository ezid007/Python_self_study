display = ['_' for _ in chosen_word]

turn = 10

while True:

    clear()
    hang_print()
    print(hangman[turn-1])
    guess = input('문자를 입력 하시오.: ').lower()

    for i, char in enumerate(chosen_word):
        if char == guess:
            display[i] = char
            print('Right')
        else:
            print('wrong')
    
    turn -= 1
     
    print(f'your turn : {turn}')
    
    if '_' not in display:
        clear_screen()
        hang_print()
        print('you win')
        break
            
    if turn < 1 :
        clear_screen()
        print(f'[{chosen_word}]')
        print('Game over')
        break
    
